/**
 * This enum represents the possible difficulty levels available in the game.
 */
public enum Difficulty {
    TUTORIAL, // A very easy difficulty level designed to teach the game mechanics
    EASY,     // An easy difficulty level with reduced challenge
    MEDIUM,   // A moderate difficulty level that provides a balanced challenge
    HARD,     // A difficult difficulty level that provides a greater challenge
    CUSTOM    // custom levels
}
