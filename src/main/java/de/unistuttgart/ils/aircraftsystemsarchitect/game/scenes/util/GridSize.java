package de.unistuttgart.ils.aircraftsystemsarchitect.game.scenes.util;

/**
 * A simple class to represent the size of a grid.
 */
public class GridSize {

    /**
     * The width of the grid.
     */
    public int x;

    /**
     * The height of the grid.
     */
    public int y;

    /**
     * Constructs a new `GridSize` object with the specified width and height.
     *
     * @param x the width of the grid
     * @param y the height of the grid
     */
    public GridSize(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
