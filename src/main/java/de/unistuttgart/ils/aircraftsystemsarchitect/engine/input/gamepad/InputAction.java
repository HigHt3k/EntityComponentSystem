package de.unistuttgart.ils.aircraftsystemsarchitect.engine.input.gamepad;

/**
 * Enumeration to define the different input actions that are available
 * from the game pad, such as joystick movement and button presses
 */
public enum InputAction {
    MOVE_UP, MOVE_DOWN, MOVE_LEFT, MOVE_RIGHT,
    A, B, X, Y, RB, LB, RT, LT, SELECT, MENU,
    DPAD_LEFT, DPAD_RIGHT, DPAD_UP, DPAD_DOWN
}
